# in no az almalgarha ya True hast ya False
a = 23
b = -34
# == ,!=
print ( a == b) # check berabar boodan (equal)
print(a != b)  # check neberabari  
print( a == 23)
# < > 
print( a < b)
print(b > 100)

# > , >=
print ( a > 23)
print(a >= 23)

# <, <=
print ( a < 23)
print(a <= 23)

